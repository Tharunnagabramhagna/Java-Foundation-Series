// Java is widely used in app development.

// The code below is called boilerplate code.

public class Code_01_Intro {
    /*
     * static => it allows main to be called without creating an object
     * of the class
     */
    public static void main(String args[]) { // entry point
        // Input and output
        System.out.print("1st-Hello World\n"); // no default newline => print
        System.out.println("2nd-Hello World"); // adds a default newline => println
        System.out.print("square of 5 : ");
        System.out.println(5 + 20);

        // Practice question:

        // 1) Print the pattern below using Java:
        /*
         * *****
         ****
         ***
         **
         *
         */
        // Answer:
        System.out.println("\nStar pattern: ");
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
    }
}
